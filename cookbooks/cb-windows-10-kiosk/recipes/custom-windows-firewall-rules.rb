# Custom Windows Firewall Rules
# Rules to apply once the CIS rules are applied

# Rules should have the following items:
# Name
# DisplayName
# Description
# Group
# Profile (Any/Domain/Private/Public/NotApplicable)
# Direction (Inbound/Outbound)
# Action (Allow/Block)
# LocalAddress
# RemoteAddress
# Protocol
# LocalPort
# RemotePort
# Program
# Service

